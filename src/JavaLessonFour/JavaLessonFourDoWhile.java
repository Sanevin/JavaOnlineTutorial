package JavaLessonFour;

public class JavaLessonFourDoWhile {
	
	public static void main(String[] args){
		
		
		int k = 1;
		
		do
		{
			System.out.println(k);
			k++;
			
		} while (k < 10);
		
		
		// for (declare iterator; conditional; change iterator
		
		for (int l=10; l>= 1; l--)
		{
			System.out.println(l);
		}
		
		
	}

}
